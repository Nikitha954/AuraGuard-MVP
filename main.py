# helper to pull human-readable EXIF data

def extract_exif_dict(image: Image.Image) -> dict:
    """Return a simple mapping of EXIF tag names to values.

    Uses :meth:`PIL.Image.Image.getexif` which is the modern API.  Some
    images (or blank canvases) may not support EXIF at all, so we catch
    ``AttributeError`` and just return an empty dict.
    """
    try:
        raw = image.getexif()
    except AttributeError:
        return {}
    if not raw:
        return {}
    exif_data = {}
    for tag, value in dict(raw).items():
        decoded = ExifTags.TAGS.get(tag, tag)
        exif_data[decoded] = value
    return exif_data


def format_gps_info(gps_info: dict) -> str:
    # gps_info is a dictionary with numeric keys corresponding to GPS tags
    # we will just stringify it for now
    try:
        # convert rational tuples to floats for readability
        def _convert(coord):
            return tuple(float(x[0]) / float(x[1]) if isinstance(x, tuple) else x for x in coord)
        entries = []
        for k, v in gps_info.items():
            name = ExifTags.GPSTAGS.get(k, k)
            entries.append(f"{name}: {_convert(v)}")
        return "; ".join(entries)
    except Exception:
        return str(gps_info)


def apply_anti_ai_shield(image: Image.Image, intensity: int = 1) -> Image.Image:
    arr = np.array(image).astype(np.int16)
    noise = np.random.randint(-intensity, intensity + 1, arr.shape, dtype=np.int16)
    arr = arr + noise
    arr = np.clip(arr, 0, 255).astype(np.uint8)
    return Image.fromarray(arr)



