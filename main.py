            with tempfile.NamedTemporaryFile(delete=False, suffix=suffix) as tmp:
                tmp.write(video_file.read())
                video_path = tmp.name
            try:
                hashes = video_phash(video_path)
                st.write(f"Computed {len(hashes)} frame hashes for video.")
                thresh = st.slider("Max Hamming distance for similarity", 0, 64, default_thresh, key="video_thresh_local")
                known_vids = load_known_video_hashes(KNOWN_VIDEO_HASHES_PATH)
                found = []
                for idx, khashes in enumerate(known_vids):
                    try:
                        d = video_hamming_distance(hashes, khashes)
                        if d <= thresh:
                            found.append((idx, d))
                    except Exception:
                        continue
                if found:
                    st.error("⚠️ This video is similar to stored banned videos:")
                    for idx, d in found:
                        st.write(f"- entry #{idx} distance {d}")
                else:
                    st.success("No similar banned video found.")
                if st.button("Ban this video (store hashes)", key="ban_video_tab"):
                    # store to the existing JSON file for compatibility
                    known_vids.append(hashes)
                    save_known_video_hashes(KNOWN_VIDEO_HASHES_PATH, known_vids)
                    # also store into the sqlite DB with a helpful name and metadata
                    try:
                        vid_id = insert_video_hashes(db_conn, video_file.name, hashes, meta={"source": "uploaded_from_UI"})
                        st.success(f"Video hashes added to banned list (db id={vid_id}).")
                    except Exception as e:
                        st.warning(f"Saved to JSON but failed to insert to DB: {e}")
            except Exception as e:
                st.warning(f"Could not process video: {e}")

